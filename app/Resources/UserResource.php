<?php 

namespace App\Resources;

use Core\Resource\AbstractResource;

class UserResource extends AbstractResource
{
    public function toArray(): array
    {
        return [
            'id' => $this->id,
            'name' => $this->name,
            'email' => $this->email,
            'created_at' => $this->created_at,
            'updated_at' => $this->updated_at,
        ];
    }
}
